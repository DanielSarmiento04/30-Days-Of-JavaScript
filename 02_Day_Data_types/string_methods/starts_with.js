// startsWith: it takes a substring as an argument and it checks if the string starts with that specified substring. It returns a boolean(true or false).
// string.startsWith(substring)
let string = 'Love is the best to in this world';
let country = 'Finland';

let des1 = string.startsWith('Lov');//if string begin with Love 'L'
console.log(des1);

let des2 = string.startsWith('lov');//if string begin with Love 'l'
console.log(des2);

let des3 = country.startsWith('Fin');//if string begin with Love 'L'
console.log(des3);

